package com.rnchess.LineCheckUtils;

public class SitesConfigBean {
    public String sid;
    public String s;
    public String code;
    public String recommendUserInputCode;
    public String jpushAppkey;

    public String getSid() {
        return sid;
    }

    public void setSid(String sid) {
        this.sid = sid;
    }

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getRecommendUserInputCode() {
        return recommendUserInputCode;
    }

    public void setRecommendUserInputCode(String recommendUserInputCode) {
        this.recommendUserInputCode = recommendUserInputCode;
    }

    public String getJpushAppkey() {
        return jpushAppkey;
    }

    public void setJpushAppkey(String jpushAppKey) {
        this.jpushAppkey = jpushAppKey;
    }
}
